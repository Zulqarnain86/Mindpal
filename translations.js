// Get current language or default to English
function getCurrentLanguage() {
  return localStorage.getItem("language") || "en";
}

// Get translation for a key
function t(key, lang = null) {
  lang = lang || getCurrentLanguage();
  
  // Map language names to language codes
  const langMap = {
    "🇬🇧 English": "en",
    "🇨🇳 中文 (Chinese)": "zh",
    "🇮🇳 தமிழ் (Tamil)": "ta",
    "🇧🇩 বাংলা (Bengali)": "bn",
    "🇮🇩 Bahasa Indonesia": "id"
  };
  
  // If lang is a language name, convert to code
  if (langMap[lang]) {
    lang = langMap[lang];
  }
  
  return translations[lang]?.[key] || translations["en"][key] || key;
}

// Update entire page language
function updatePageLanguage(lang) {
  localStorage.setItem("language", lang);
  location.reload();
}

// Get random quote in current language
function getRandomQuote(lang = null) {
  lang = lang || getCurrentLanguage();
  
  const langMap = {
    "🇬🇧 English": "en",
    "🇨🇳 中文 (Chinese)": "zh",
    "🇮🇳 தமிழ் (Tamil)": "ta",
    "🇧🇩 বাংলা (Bengali)": "bn",
    "🇮🇩 Bahasa Indonesia": "id"
  };
  
  if (langMap[lang]) {
    lang = langMap[lang];
  }
  
  const quotes = translations[lang]?.quotes || translations["en"].quotes;
  return quotes[Math.floor(Math.random() * quotes.length)];
}
